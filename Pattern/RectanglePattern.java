public class RectanglePattern {
    public static void main(String args[]){
        for (int i=1;i<5;i++){  // This is Nested loop   (for under for loop)
            for (int j=1;j<6;j++){
                System.out.print("*");
            }
        System.out.println();
        }
    }
}
